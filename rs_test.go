package main

import (
	"testing"
)

func Test1(t *testing.T) {
	rs := RS{}
	prim := 0x11d
	rs.InitTables(prim)
	msgIn := []uint{0x40, 0xd2, 0x75, 0x47, 0x76, 0x17, 0x32, 0x06, 0x27, 0x26, 0x96, 0xc6, 0xc6, 0x96, 0x70, 0xec}
	result := rs.RsEncodeMsg(msgIn, 10)
	msgOut := []int{0xbc, 0x2a, 0x90, 0x13, 0x6b, 0xaf, 0xef, 0xfd, 0x4b, 0xe0}
	for i := 0; i < len(result); i++ {
		if result[i] != msgOut[i] {
			t.Errorf("rs.RsEncodeMsgError: index: %d, result: %0x, msgOut: %0x", i, result[i], msgOut[i])
		}
	}
}
